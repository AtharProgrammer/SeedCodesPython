'''
in operator:
The in operator checks if a value exists in a sequence.
It returns True if the value is present in the sequence, and False otherwise.
'''

# Example with strings
my_string = "Hello, world!"
print('He' in my_string)  # Output: True
print('z' in my_string)  # Output: False


'''
not in operator:
The not in operator is the negation of the in operator. It returns
True if the value is not present in the sequence, and False if it is present.
'''


# Example with strings
my_string = "Hello, world!"
print('z' not in my_string)  # Output: True
print('H' not in my_string)  # Output: False
