'''
List Concatenation and Repetition: You can concatenate lists using the + operator and repeat elements using the * operator.
'''

# Example 1: Concatenation
list1 = [1, 2, 3]
list2 = [4, 5, 6]
concatenated_list = list1 + list2
print(concatenated_list)  # Output: [1, 2, 3, 4, 5, 6]

# Example 2: Repetition
repeated_list = [2] * 3
print(repeated_list)  # Output: [2, 2, 2]
