'''
Slicing: Python allows you to access specific portions of a list using slicing syntax list[start:end:step].
'''

my_list = [1, 2, 3, 4, 5]
print(my_list[1:4])     # Output: [2, 3, 4]
print(my_list[::3])     # Output: [1, 3, 5]
print(my_list[::-1])    # Output: [5, 4, 3, 2, 1] (reverse the list)
