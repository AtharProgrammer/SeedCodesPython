'''
Addition (+): Adds two operands.
Subtraction (-): Subtracts the right operand from the left operand.
Multiplication (*): Multiplies two operands.
Division (/): Divides the left operand by the right operand and returns a floating-point number.
Floor Division (//): Divides the left operand by the right operand, rounding down to the nearest whole number to return an integer result.
Modulus (%): Returns the remainder of the division of the left operand by the right operand.
Exponentiation (**): Raises the left operand to the power of the right operand.
'''

# Addition
print(5 + 3)  # Output: 8

# Subtraction
print(10 - 3)  # Output: 7

# Multiplication
print(4 * 7)  # Output: 28

# Division
print(8 / 2)  # Output: 4.0

# Floor Division
print(3 // 7)  # Output: 3

# Modulus
print(9 % 4)  # Output: 1

# Exponentiation
print(2 ** 3)  # Output: 8
