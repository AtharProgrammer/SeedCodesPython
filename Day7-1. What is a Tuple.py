'''
What is a Tuple?
A tuple is a collection of ordered and immutable elements.
In simpler terms, it's a sequence of values that cannot be changed (immutable). Tuples are similar to lists,
but they are immutable, which means you cannot add, remove, or modify elements after the tuple is created.
'''


#Creating Tuples
#You can create a tuple by placing comma-separated values inside parentheses ().
my_tuple = (1, 2, 3, 4, 5)
print(my_tuple)


#You can also create an empty tuple using empty parentheses:
empty_tuple =()
print(empty_tuple)


#Accessing Elements
#You can access elements in a tuple using indexing, just like with lists.
my_tuple = (1, 2, 3, 4, 5)
print(my_tuple[0])  # Output: 1
print(my_tuple[2])  # Output: 3
