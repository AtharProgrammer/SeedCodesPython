# Creating a nested dictionary
student_records = {
    "John": {"age": 25, "grade": "A"},
    "Alice": {"age": 22, "grade": "B"},
    "Bob": {"age": 24, "grade": "C"}
}

# Accessing values in nested dictionaries
print(student_records["John"]["age"])  # Output: 25
print(student_records["Alice"]["grade"])  # Output: B


