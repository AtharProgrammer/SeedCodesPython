# Overview of key OOP concepts in Python

'''
Classes and Objects:

A class is a blueprint for creating objects. It defines the attributes and methods that objects of the class will have.
An object is an instance of a class. It is a concrete realization of the class blueprint, with its own unique data and behavior.

Attributes:
Attributes are data members of a class that hold information about the object.
They are accessed using dot notation (object.attribute).

Methods:
Methods are functions defined within a class that perform operations on objects of that class.
They can access and modify the object's attributes.
Constructor (__init__):

The __init__ method is a special method in Python classes that is called when a new object of the class is created.
It is used to initialize the object's attributes.

Inheritance:

Inheritance is a mechanism where a new class (subclass) is derived from an existing class (superclass).
The subclass inherits attributes and methods from the superclass.
It allows for code reuse and promotes modularity and extensibility.
Encapsulation:

Encapsulation refers to the bundling of data and methods that operate on the data into a single unit (i.e., a class).
It hides the internal state of objects and only exposes the necessary functionality through methods.
Polymorphism:

Polymorphism allows objects of different classes to be treated as objects of a common superclass.
It enables code to work with objects of multiple types through a uniform interface.

'''



