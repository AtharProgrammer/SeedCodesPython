'''
Basic Assignment Operator
=: Assigns the value on the right to the variable on the left.
'''
x = 10

'''
Compound Assignment Operators
+= (Add AND): Adds the right operand to the left operand and assigns the result to the left operand.
'''
x += 5  # Equivalent to x = x + 5

'''
-= (Subtract AND): Subtracts the right operand
from the left operand and assigns the result to the left operand.
'''

x -= 5  # Equivalent to x = x - 5

'''
*= (Multiply AND): Multiplies the right operand with the left operand and
assigns the result to the left operand.
'''
x *= 5  # Equivalent to x = x * 5

'''
/= (Divide AND): Divides the left operand by the right operand and assigns the
result to the left operand.
'''

x /= 5  # Equivalent to x = x / 5

'''
%= (Modulus AND): Takes the modulus using the two operands and assigns the result to the left operand.
'''
x %= 5  # Equivalent to x = x % 5

'''
//= (Floor Division AND): Performs floor division on the operands and assigns the result to the left operand.
'''
x //= 5  # Equivalent to x = x // 5

'''
**= (Exponent AND): Performs exponential (power) calculation on the operands and assigns the result to the left operand.
'''
x **= 5  # Equivalent to x = x ** 5



