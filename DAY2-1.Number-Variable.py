#Creating Number variable-

#Integer
my_integer = 10
print(my_integer)  # Output: 10

#Floating-Point Number
my_float = 20.5
print(my_float)  # Output: 20.5

#Complex Number
my_complex = 3 + 4j
print(my_complex.real)  # Output: 3.0
print(my_complex.imag)  # Output: 4.0


#Operations with Number Variables

# Addition
result = 5 + 3
print(result)  # Output: 8

# Division
result = 8 / 2
print(result)  # Output: 4.0

# Floor Division
result = 5 // 2
print(result)  # Output: 2

# Exponentiation
result = 2 ** 3
print(result)  # Output: 8

#Example of Type Conversion:
# Convert float to int
my_int = int(10.6)
print(my_int)  # Output: 10

# Convert int to float
my_float = float(20)
print(my_float)  # Output: 20.0

# Convert to complex
my_complex = complex(2, 3)
print(my_complex)  # Output: (2+3j)





