'''
List Methods:
Python provides several built-in methods to manipulate lists:

append(): Adds an element to the end of the list.
extend(): Adds all elements of another list to the end of the list.
insert(): Inserts an element at a specified position.
remove(): Removes the first occurrence of a specified value.
pop(): Removes and returns the element at a specified index (default is the last element).
clear(): Removes all elements from the list.
index(): Returns the index of the first occurrence of a value.
count(): Returns the number of occurrences of a value.
sort(): Sorts the list.
reverse(): Reverses the order of the elements in the list.

'''

#append(): Adds an element to the end of the list.
my_list = [1, 2, 3]
my_list.append(4)
print(my_list)  # Output: [1, 2, 3, 4]
